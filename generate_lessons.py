import json
import os

def build_lesson(lesson_tuple):
    lid, title, desc, vocab_list, grammar_list, sent_list = lesson_tuple

    vocab = [
        {"id": f"{lid}_{vid}", "japanese": vjp, "english": ven, "partOfSpeech": vpos}
        for vid, vjp, ven, vpos in vocab_list
    ]

    grammar = [
        {"pattern": gp, "explanation": ge, "example": gex}
        for gp, ge, gex in grammar_list
    ]

    sentences = []
    for item in sent_list:
        sid, sen, sjp = item[0], item[1], item[2]
        gnote = item[3] if len(item) > 3 else None
        entry = {"id": f"{lid}_{sid}", "english": sen, "japanese": sjp}
        if gnote:
            entry["grammarNote"] = gnote
        sentences.append(entry)

    return {
        "id": lid,
        "title": title,
        "description": desc,
        "vocabulary": vocab,
        "grammar": grammar,
        "sentences": sentences,
    }


def main():
    output = [build_lesson(l) for l in lessons]

    stats = {
        "lessons": len(output),
        "vocabulary": sum(len(l["vocabulary"]) for l in output),
        "grammar": sum(len(l["grammar"]) for l in output),
        "sentences": sum(len(l["sentences"]) for l in output),
    }
    print(f"Generated {stats['lessons']} lessons:")
    print(f"  vocabulary: {stats['vocabulary']} words")
    print(f"  grammar:    {stats['grammar']} points")
    print(f"  sentences:  {stats['sentences']} examples")

    # Write JSON
    script_dir = os.path.dirname(os.path.abspath(__file__))
    data_dir = os.path.join(script_dir, "data") if script_dir else "data"
    outpath = os.path.join(data_dir, "lessons.json")

    with open(outpath, "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f"\nWritten to {outpath}")
    print(f"File size: {os.path.getsize(outpath):,} bytes")


if __name__ == "__main__":
    main()
